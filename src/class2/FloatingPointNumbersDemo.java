package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /* by default all the floating point numb ers are doubles,
        that's why we need to write 'F' or 'f' at the end of the floating type variables

         */
        float grocery=20.5F; // is a smaller size data type
        System.out.println(grocery);
        double price=11111.23542546; //is bigger in size can hold double the amount of floating
        System.out.println(price);



    }
}
