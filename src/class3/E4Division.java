package class3;

public class E4Division {
    public static void main(String[] args) {

        System.out.println(10/5);

        System.out.println(11/5); //java understands that these are int/int. int can't store remains part

        System.out.println(11.0/5.0);//java understands that it's a double data type

        int number1=10;
        int number2=4;
        System.out.println(number1/number2);

        double num1=10;
        double num2=4;
        System.out.println(number1/number2);
        System.out.println(num1/num2);
    }
}
