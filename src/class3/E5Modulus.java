package class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1=11;
        int number2=4;
        System.out.println(number1/number2);
        System.out.println(number1%number2);

        int noOfSlides=5;
        int noOfIndividuals=3;
        System.out.println(noOfSlides/noOfIndividuals);
        System.out.println(noOfSlides%noOfIndividuals);
    }
}
