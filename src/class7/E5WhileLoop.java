package class7;

public class E5WhileLoop {
    public static void main(String[] args) {

        //print these numbers 2 4 6 8 10 12 14 using a while loop
        int num=2;
        while (num<=14){
            System.out.println(num);
            num++;
            num++; // equals to counter+=2, counter=counter+2
        }
    }
}
