public class E9WhileLoop {
    public static void main(String[] args) {

        //print 25 20  10 5 using loop and if condition
        int num =25;
        while (num >= 5) {
            if(num!=15) {
                System.out.print(num + " ");
            }
            num = num - 5;

        }
    }
}
