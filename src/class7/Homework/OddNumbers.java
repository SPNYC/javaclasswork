public class OddNumbers {
    public static void main(String[] args) {

        int num=20;

        while (num<=50){
            if (num%2!=0){
                System.out.print(num+" ");
            }
            num++;
        }
        System.out.println();

        for (int i =20; i <=50 ; i++) {
            if(i%2!=0) {
                System.out.print(i+" ");
            }
        }
        }
    }

